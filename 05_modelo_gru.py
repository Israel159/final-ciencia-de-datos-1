"""## Justificación técnica del modelo GRU

Se selecciona **GRU** como tercera arquitectura por las siguientes razones:

1. **Eficiencia paramétrica**: GRU tiene aproximadamente un 25-30% menos de parámetros que LSTM al fusionar el cell state y hidden state en un único vector. Esto reduce el tiempo de entrenamiento y la memoria requerida, siendo ideal para datasets de tamaño medio como el actual (~35,000 registros horarios).

2. **Capacidad de modelado comparable**: Aunque más simple, las compuertas de actualización (update gate) y reinicio (reset gate) de GRU permiten capturar dependencias de largo plazo con eficacia similar a LSTM en problemas donde los ciclos temporales son claros y estables (como el consumo eléctrico residencial).

3. **Comparabilidad controlada**: Se mantienen idénticos los hiperparámetros de entrenamiento (epochs, batch size, callbacks, lookback) y el pipeline de features respecto a RNN y LSTM. Cualquier diferencia en métricas se atribuye exclusivamente a la arquitectura.

4. **Trade-off velocidad-precisión**: GRU ofrece un punto intermedio entre la simplicidad de RNN (rápida pero limitada) y la complejidad de LSTM (potente pero más pesada). Si GRU alcanza métricas similares a LSTM con menor costo computacional, demuestra que el problema no requiere la sobrecarga paramétrica de tres compuertas.

**Hipótesis esperada**: GRU debería obtener métricas intermedias entre RNN y LSTM, o muy cercanas a LSTM, validando que el consumo eléctrico es un problema donde la eficiencia paramétrica puede compensar la menor complejidad de compuertas.
"""