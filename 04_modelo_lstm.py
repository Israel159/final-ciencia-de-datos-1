"""## Justificación técnica del modelo LSTM

Se selecciona **LSTM** como segunda arquitectura por las siguientes razones:

1. **Mitigación del vanishing gradient**: A diferencia de SimpleRNN, donde el gradiente se atenúa exponencialmente en secuencias largas, LSTM utiliza un **cell state** con compuertas (input, forget, output) que preservan información relevante durante cientos de timesteps. Esto es crítico para capturar patrones semanales (168h) y estacionales.

2. **Memoria selectiva**: La compuerta de olvido permite descartar información irrelevante (ej. ruido de una hora anómala) sin afectar el estado de largo plazo, mientras que la compuerta de entrada incorpora nueva información solo cuando es significativa.

3. **Comparabilidad controlada**: Se mantienen idénticos los hiperparámetros de entrenamiento (epochs, batch size, callbacks, lookback) y el pipeline de features respecto al modelo RNN. Cualquier mejora en MAE/RMSE/R² se atribuye exclusivamente a la arquitectura LSTM, no a diferencias en preprocesamiento o tuning.

4. **Aplicabilidad al dominio**: El consumo eléctrico residencial presenta dependencias de largo plazo (ej. la calefacción encendida hace 48 horas influye en la temperatura actual de la vivienda). LSTM captura estas inercias térmicas mejor que RNN.

**Hipótesis esperada**: Dado que el problema presenta ciclos diarios y semanales claros, LSTM debería superar a RNN en al menos un 10-20% en RMSE, demostrando que el problema sí requiere memoria de largo plazo.
"""