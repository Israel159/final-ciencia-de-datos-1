"""## 3. Arquitectura del modelo LSTM

Se implementa una red **LSTM** de dos capas con dropout:

| Capa | Tipo | Unidades | Activación | Propósito |
|---|---|---|---|---|
| 1 | LSTM | 64 | tanh | Memoria de largo plazo con compuertas |
| 2 | Dropout | — | — | Regularización (20%) |
| 3 | LSTM | 32 | tanh | Refinamiento de patrones temporales |
| 4 | Dropout | — | — | Regularización (20%) |
| 5 | Dense | 16 | relu | Capa intermedia no lineal |
| 6 | Dense | 1 | lineal | Predicción de potencia activa escalada |

**Optimizador:** Adam (lr=0.001).  
**Pérdida:** MSE.  
**Métrica:** MAE.

La clave respecto a RNN es que LSTM mantiene un **cell state** separado del hidden state, permitiendo que el gradiente fluya a través de cientos de timesteps sin degradarse.
"""

# ============================================================
# ARQUITECTURA LSTM
# ============================================================

tf.random.set_seed(42)
np.random.seed(42)

model_lstm = Sequential([
    LSTM(64, activation='tanh', return_sequences=True,
         input_shape=(LOOKBACK, len(feature_cols))),
    Dropout(0.2),
    LSTM(32, activation='tanh'),
    Dropout(0.2),
    Dense(16, activation='relu'),
    Dense(1)
])

model_lstm.compile(
    optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
    loss='mse',
    metrics=['mae']
)

print("=" * 60)
print("ARQUITECTURA LSTM")
print("=" * 60)
model_lstm.summary()

"""## 4. Entrenamiento con callbacks

Mismos mecanismos de control que el modelo RNN para garantizar comparabilidad:
- **EarlyStopping**: paciencia 10 épocas, restaura mejores pesos.
- **ReduceLROnPlateau**: reduce lr a la mitad si estanca 5 épocas.
- **ModelCheckpoint**: guarda el mejor modelo por `val_loss`.

**Hiperparámetros:**
- Epochs máximas: 100
- Batch size: 32
- Validación: año 2009
"""

