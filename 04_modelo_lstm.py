"""## 2. Construcción de secuencias temporales (ventanas deslizantes)

Las redes LSTM reciben tensores 3D: `(samples, timesteps, features)`. Se mantiene `lookback=24` horas para predecir la siguiente hora, garantizando comparabilidad directa con el modelo RNN.

**Features de entrada:**
- Variables escaladas (`*_scaled`)
- Codificaciones cíclicas (`Hour_sin/cos`, `DayOfWeek_sin/cos`, `Month_sin/cos`)
- Variable binaria `IsWeekend`

La variable objetivo es `Global_active_power_scaled`.
"""

# ============================================================
# CREACIÓN DE SECUENCIAS TEMPORALES
# ============================================================

LOOKBACK = 24  # 24 horas de historia → predicción de la hora 25

def create_sequences(df, lookback=24):
    scaled_cols = [c for c in df.columns if c.endswith('_scaled')]
    cyclic_cols = ['Hour_sin', 'Hour_cos', 'DayOfWeek_sin', 'DayOfWeek_cos',
                   'Month_sin', 'Month_cos']

    feature_cols = sorted(set(scaled_cols + cyclic_cols + ['IsWeekend']))

    missing = [c for c in feature_cols if c not in df.columns]
    if missing:
        raise ValueError(f"❌ Columnas faltantes: {missing}")

    data = df[feature_cols].values.astype(np.float32)
    target = df['Global_active_power_scaled'].values.astype(np.float32)

    X, y = [], []
    for i in range(len(data) - lookback):
        X.append(data[i : i + lookback])
        y.append(target[i + lookback])

    return np.array(X), np.array(y), feature_cols

X_train, y_train, feature_cols = create_sequences(train_df, LOOKBACK)
X_val,   y_val,   _           = create_sequences(val_df, LOOKBACK)
X_test,  y_test,  _           = create_sequences(test_df, LOOKBACK)

