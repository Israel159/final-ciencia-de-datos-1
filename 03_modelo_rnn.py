"""## Justificación técnica del modelo RNN

Se selecciona una **Red Neuronal Recurrente (SimpleRNN)** como baseline del proyecto por las siguientes razones:

1. **Memoria temporal explícita**: A diferencia de redes densas tradicionales, las RNN mantienen un estado oculto que se propaga a través de la secuencia, permitiendo capturar dependencias temporales en el consumo eléctrico (inercia térmica, hábitos de uso).

2. **Baseline interpretable**: SimpleRNN es la arquitectura recurrente más elemental. Si LSTM o GRU superan significativamente sus métricas, se demuestra cuantitativamente que el problema requiere mecanismos de compuertas (olvido/control de flujo) más sofisticados.

3. **Entrada multivariada**: En cada paso de tiempo, el modelo recibe un vector con todas las variables predictoras escaladas (potencias, voltaje, submediciones, medias móviles, diferencias) junto con codificaciones cíclicas, permitiendo aprender interacciones no lineales entre predictores.

4. **Regularización**: Las capas `Dropout(0.2)` mitigan el sobreajuste en un dataset de ~35,000 registros horarios, forzando al modelo a aprender representaciones robustas.

5. **Optimización adaptativa**: `ReduceLROnPlateau` ajusta dinámicamente la tasa de aprendizaje cuando la validación estanca, evitando oscilaciones alrededor del mínimo.

6. **Forecasting autoregresivo**: Las predicciones futuras se generan iterativamente, reinyectando cada predicción como input del siguiente paso. Este esquema simula condiciones reales de despliegue donde no se dispone de valores futuros observados.

**Limitaciones conocidas**: SimpleRNN puede sufrir del problema de *vanishing gradients* en secuencias largas. Si el modelo no captura dependencias de largo plazo (ej. estacionalidad anual), LSTM/GRU deberían mejorar el R² sustancialmente, lo cual se verificará en los notebooks 04 y 05.
"""