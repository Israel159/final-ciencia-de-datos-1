"""## 7. Predicciones futuras (Forecasting)

Se generan predicciones más allá del último dato disponible mediante un esquema **autoregresivo iterativo**:

1. Se toma la última ventana conocida del test set (24 horas).
2. El modelo predice el siguiente timestep.
3. Se actualiza la ventana deslizante incorporando la predicción como nuevo input.
4. Se repite el proceso para 24 horas (1 día) y 168 horas (1 semana).

**Nota metodológica:** En un forecasting real multivariado, las demás variables también deberían proyectarse. Para este ejercicio, se mantienen constantes las variables exógenas y se actualiza únicamente la variable objetivo, lo cual es una aproximación conservadora válida para horizontes cortos.
"""

